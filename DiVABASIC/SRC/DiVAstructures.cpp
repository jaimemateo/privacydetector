#include "DiVAstructures.h"

/**
 *	This method allow to create a DiVASCALAR from double values
 *
 * \param val0 First value for a DiVASCALAR
 * \param val1 Second value for a DiVASCALAR
 * \param val2 Third value for a DiVASCALAR
 * \param val3 Fourth value for a DiVASCALAR
 * \return Return a DiVASCALAR
 */
DiVASCALAR DiVAScalar(double val0, double val1, double val2, double val3){
	
	DiVASCALAR valor;
	
	valor.val[0] = val0;
	valor.val[1] = val1;
	valor.val[2] = val2;
	valor.val[3] = val3;

	return valor;
}

/**
 *	This method allow to create a DiVASCALAR from single double value
 *
 * \param val0123 Single value for a DiVASCALAR
 *
 * \return Return a DiVASCALAR
 */
DiVASCALAR DiVAScalarAll(double val0123){
	
	DiVASCALAR valor;
	
	valor.val[0] = val0123;
	valor.val[1] = val0123;
	valor.val[2] = val0123;
	valor.val[3] = val0123;

	return valor;
}
